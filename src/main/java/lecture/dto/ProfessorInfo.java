package lecture.dto;

public class ProfessorInfo {
	
	String pf_name;
	String pf_dept;
	int pf_no;
	
	
	public String getPf_name() {
		return pf_name;
	}
	public void setPf_name(String pf_name) {
		this.pf_name = pf_name;
	}
	public String getPf_dept() {
		return pf_dept;
	}
	public void setPf_dept(String pf_dept) {
		this.pf_dept = pf_dept;
	}
	public int getPf_no() {
		return pf_no;
	}
	public void setPf_no(int pf_no) {
		this.pf_no = pf_no;
	}
}
